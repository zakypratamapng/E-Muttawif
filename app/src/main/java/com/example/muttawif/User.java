package com.example.muttawif;

public class User {
    public String uid, nama, email, noTelepon, alamat, noRombongan;

    public User() {
    }

    public User(String uid, String nama, String email, String noTelepon, String alamat, String noRombongan) {
        this.uid = uid;
        this.nama = nama;
        this.email = email;
        this.noTelepon = noTelepon;
        this.alamat = alamat;
        this.noRombongan = noRombongan;
    }
}
